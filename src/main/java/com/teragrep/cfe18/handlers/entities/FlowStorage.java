package com.teragrep.cfe18.handlers.entities;

import com.fasterxml.jackson.annotation.JsonInclude;
import io.swagger.v3.oas.annotations.media.Schema;

@JsonInclude(JsonInclude.Include.NON_NULL)
public class FlowStorage {
    @Schema(accessMode = Schema.AccessMode.READ_ONLY)
    private int id;
    private int flowId;
    @Schema(accessMode = Schema.AccessMode.READ_ONLY)
    private Storage.StorageType storageType;
    @Schema(accessMode = Schema.AccessMode.READ_ONLY)
    private String storageName;
    private int storageId;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFlowId() {
        return flowId;
    }

    public void setFlowId(int flowId) {
        this.flowId = flowId;
    }

    public Storage.StorageType getStorageType() {
        return storageType;
    }

    public void setStorageType(Storage.StorageType storageType) {
        this.storageType = storageType;
    }

    public String getStorageName() {
        return storageName;
    }

    public void setStorageName(String storageName) {
        this.storageName = storageName;
    }

    public int getStorageId() {
        return storageId;
    }

    public void setStorageId(int storageId) {
        this.storageId = storageId;
    }

    @Override
    public String toString() {
        return "FlowStorage{" +
                "id=" + id +
                ", flowId=" + flowId +
                ", storageType='" + storageType + '\'' +
                ", storageName='" + storageName + '\'' +
                ", storageId=" + storageId +
                '}';
    }
}
